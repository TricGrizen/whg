“正因为它如此艰险，才有了群山的一部分之美。这也是这些地方山上没多少气象站的原因之一，”佩里说。“我们也想看到它最狂暴、最难对付的样子。那也是气候的一部分，我们需要把它测量下来。”

佩里是一支国际团队的联合领队。今年二月，这支队伍冒着全球疫情，用15天在厚雪中徒步、攀登，终于在南安第斯山脉一座休眠火山——图蓬加托山——的峰顶下方装好了一座气象站。气象站坐落在智利与阿根廷交界处，是南半球和西半球海拔最高的气象站，将帮助科学家了解这一地区的气候正在以多快的速度改变。这次考察由国家地理学会组织，并得到劳力士“恒动地球”计划的支持。

借助这座气象站将采集的气温、风速和雪深数据，科学家希望能更好地了解：当气候变化让这一地区遭遇更多干旱（就像它眼下正经历的那场历史罕见的干旱），并让作为水源储备的“山地水塔”——冰川与积雪——不断萎缩时，智利中部和首都圣地亚哥将会面临怎样的处境。

“如今的利害关系真的非常重大，”考察队成员、英国拉夫堡大学气候科学家汤姆·马修斯说。“有数百万人生活在这些水塔的下游。它们属于这样一套系统：随着气候变暖，它会作何反应，我们所知甚少。”

图蓬加托山是智利第六高峰，也是迈波河流域的最高峰——这片流域为生活在圣地亚哥及周边的670万人供水。有了图蓬加托这类高山峰顶降水量的更准确数据，政府官员就能知道某一年究竟有多少水可供分配。

“从1982年起，我就一直在研究冰川。在我这一生里，我们已经看到冰川和积雪发生了巨大的变化，”考察队联合领队、智利政府冰川部门负责人希诺·卡萨萨说。

卡萨萨说，在干旱的年份，夏末汇入迈波河的水有三分之二来自正在萎缩的冰川。

智利中部属于地中海型生态区，气候与加利福尼亚等地相似。它位于地球上除极地之外最干旱之地——阿塔卡马沙漠——的南面，夹在安第斯山脉与太平洋之间。

智利人早已习惯隔一段时间就来一个旱年；2010年便是这样一年。可到了2011年，接着2012、2013年，降雨依然很少。

“然后是2014年，”那一年同样干旱，“这就有些蹊跷了，”智利大学气候学家勒内·加雷奥（未参与此次考察）说。

到2015年，加雷奥和他的智利同事已经认定，这一地区正处在他们所称的“特大干旱”之中。这场干旱已持续了十多年。自2010年干旱开始以来，平均每年的降雨都比往常少三分之一。2019年是干旱发生以来最干的一年，降雨量还不到正常年份的四分之一。

加雷奥说，十年间的降雨总量会受一些自然波动的影响，但毫无疑问，气候变化正在加剧这场特大干旱。总体而言，气候变化预计会让干旱地区更干、湿润地区更湿；虽然智利以前也经历过干旱期，却没有一次像这样严重、持续这样久。全球气温升高改变了曾经带来降水的天气型态，而各种模型显示，这些新的型态很可能会持续下去。

这对智利中部是个坏消息——它的淡水依赖迈波河流域的山地水塔。2019年发表在《自然》杂志上的一篇论文指出，从安第斯山到喜马拉雅山，全世界的水塔都正受到气候变化的威胁。

两年前，佩里和马修斯曾在珠穆朗玛峰上装过一座气象站，那是当时世界上海拔最高的气象站。这次智利之行，是国家地理学会为考察和研究地球上一些最脆弱环境的气候与环境变化而进行的最新一次远征。

登上海拔两万一千多英尺的图蓬加托峰顶，团队用了大约10天，下撤又花了5天。出发前的几个月里，队员们进行了大量训练。佩里在北卡罗来纳州背着沉重的背包，一连几个小时攀爬陡峭的阿巴拉契亚山径。在圣地亚哥东南，卡萨萨则去爬他家附近的山丘。

队员们从海拔6152英尺处开始这趟行程，那里是一片点缀着灌木丛的生态环境。随着接下来几天不断向上攀登，这幅景象很快就变了。这是一场硬仗。

团队由佩里、卡萨萨及其在智利水利部门的同事、登山向导，以及赶骡人组成——赶骡人负责把驮货的骡子和马赶上山。

“马提前我们几天先走，去探路、确认路通不通，结果雪深得连马都陷到了脖子，”佩里说。四名骑马的人加上驮货的骡子走在队伍前头。不仅山路陡峭，气温还回升得刚好够呛——登山者没法在雪面上行走，一脚踩下去就陷了进去。“有一回我迈出一步，等回过神来，人已经陷到腰那么深了，”佩里说。

团队扛上图蓬加托山的这座气象站，是一具重120磅、高6英尺、可折叠的铝制三脚架。它足够轻，可以拆成零件装进几个背包分别背上山，又应当足够结实，能扛住世界上最强的一些大风。佩里说，队伍在雪里艰难跋涉时，渐渐看清马匹没法爬到他们原本指望的那么高。于是卡萨萨叫来一架直升机，把气象站送完剩下的那段上山路程。

要把这台仪器固定在峰顶附近，得把气象站的三脚架用螺栓锚进岩石，再用拉索打桩固定，好让它稳住。气象站靠太阳能充电的电池供电，还装了一根用于卫星通信的天线。佩里说，它已经记录到每小时超过116英里的风速。

科学家们还在峰顶附近的永久冻土里、约三英尺深处埋下了温度传感器，用来追踪冻土的温度变化。气象站将测量辐射、雪深，以及反照率（也就是反射率）。反照率之所以重要，是因为随着降雪减少、冰层消融，会露出反照率较低的积雪，最终露出深色的岩石，从而抬高周围地表的温度，可能进一步加快融化。

“随着气候变暖，冰川会退缩得相当快，”马修斯说。“到底有多快？我们不知道。以往大多数观测都是在山上海拔相当低的地方做的，所以对上面三分之一那段究竟在发生什么，我们缺乏了解。”

马修斯说，要判断智利在群山中封存了多少淡水、又会在何时降到危险的低位，都是十分复杂的预测。短期看，加快冰雪消融的热量会带来更多的水，可能引发洪灾。但随着消融加速，冰川最终会“缩得太小，尽管化得很快，可供融化的也所剩无几”，于是径流也随之减少，他说。

科学家把这个转折点称为“水峰”——短期内奔涌而来的水，就在这一刻转为更长期的短缺。

迈波河流域另外只有两座高海拔气象站，卡萨萨希望这座新站能成为他和同事将来遍布智利各地所建众多气象站中的一座。

尽管美国以及包括智利在内的其他国家都承诺要减少加剧气候变化的排放，智利中部仍然必须为最坏的情形做好准备。

加雷奥不太愿意说圣地亚哥会迎来水源枯竭的“归零日”——2018年，南非开普敦的居民就曾担心那样的日子会降临自己的城市。他乐观地相信，这一地区能够适应，办法是更高效地用水、并压缩消耗。

在圣地亚哥西北，一家私营公司正在兴建第一座通用型海水淡化厂，可为饮用、农业和采矿供水。智利天主教大学土木工程师塞巴斯蒂安·维库尼亚（研究该国如何适应气候变化）说，这是为智利中部确保稳定淡水供应的众多手段之一。

当地的自来水公司和市政当局也在采取各种办法，比如在迈波河的一条支流上筑坝蓄水，以及用卡车把饮用水运给那些水井已经干涸的人。

维库尼亚和同事建立的模型发现，这场干旱应会持续到本世纪中叶，而要保证圣地亚哥有可靠的饮用水，就得以牺牲供给农民的水为代价。维库尼亚说，眼下冰川正在退缩，但“它们每年仍是非常可靠的水源，尤其是在干旱期间。”如果冰川最终如预料般消失，那一份储备的水也会随之而去。

国家地理学会致力于照亮并守护我们这个世界的奇迹。欲进一步了解学会对旗下探险家的支持，请见此。

