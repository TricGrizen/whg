这一两年来，黑猩猩越来越靠近人的居所，在几乎整个基亚马贾卡村四处游荡，寻找食物，从树上扯香蕉，抓芒果、木瓜，以及任何能勾起它们食欲的东西。它们还擅自吃掉了塞马塔家附近一棵树上的菠萝蜜。但2014年7月20日，令人胆寒的种种磨难演变成了一场恐怖——一种也降临在其他乌干达家庭身上的恐怖。就在那一天，一只大黑猩猩（很可能是成年雄性）抓走了塞马塔家蹒跚学步的小儿子穆朱尼，并把他杀死了。

