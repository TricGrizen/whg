多亏了保护区的守护，圆岛依旧是数千头太平洋海象、以及超过25万只筑巢海鸟的季节性家园。在托吉亚克苦等了五天、避过风暴天气之后，当我们乘船靠近时，这座岛几乎像被施了魔法：一座陡峭的绿色穹丘从海里拔起，峰顶笼罩在雾中。

玛格丽特·阿奇博尔德喜欢用哪些野花正在盛开来记录圆岛的季节。她是每年夏天驻守岛上的两名阿拉斯加渔猎局技术员之一，前来迎接我们船只的她，穿着黑色牛仔裤和运动衫，一头赤褐色的头发凌乱地压在棒球帽下。

在岛上度过的六个季节里，她把这里的生态系统研究得如此细致，甚至用一块块手工小牌给野花做了标注。碎米荠。北极堇菜。眼下正值7月，轮叶马先蒿开花了：那是一种品红色、状似兰花的花，沿着我们初次上岸的步道娇柔地卷曲绽放。“要是我话太多，先跟你们道个歉，”阿奇博尔德说，“你们俩是我们几周来接待的头一批访客。”

我们的船靠岸时，登陆的海滩上挤满了熟睡的海象。为了不惊扰它们，我们在稍远处卸下装备，说话都压低了声音。阿奇博尔德帮我们把行李搬往营地，空气里嗡嗡地响着稀树草鹀和隐夜鸫的啁啾。绿色的苔原草地缀满野花，沿着岛上多崖的海岸绵延；对于一处严格说来属于北极苔原生物群系的地方而言，这里葱茏得出人意料。

支好帐篷后，我们跟着阿奇博尔德开始她每天的巡查。维护岛上的步道系统、照管访客项目，以及执行那条把岛与来往船只和商业捕捞隔开的三英里保护区界线，都是她的职责。

她认为自己最重要的工作，是每天清点海象、海鸟以及一群濒危的北海狮的数量。布里斯托尔湾及其渔业是关乎阿拉斯加经济的重要生态系统，而围绕圆岛的海鸟和海洋哺乳动物——它们充当着气候指示物种——所收集的数据，是这一地区最为连贯的记录之一。她解释说，随着北极气温升高，圆岛上发生的一切，为了解更广阔的海洋生态系统的动向提供了宝贵的洞见。

阿奇博尔德领着我们沿一条维护良好的步道穿过崖顶草地，一路指点着观赏筑巢海鹦和白腹海雀的好地方。她说，岛上的赤狐刚刚产下了幼崽。随后我们来到俯瞰“第一海滩”的一处观景点——一道长满草、开满羽扇豆的岩台——探身望向崖下。就在我们脚下不过一百英尺处，数百头海象粉红一片地堆叠在礁石上。我们能闻到它们的气味：咸腥的、海洋的、充满生殖气息的。我们在草地上坐下，保持安静、放低姿态，好让它们安然接受我们的存在。

圆岛非常适合习惯了恶劣天气的资深露营者。这里有八座俯瞰大海的木质帐篷平台、一间堆肥式厕所，还有一顶大炊事帐、内设野餐桌和丙烷炉——它的基础设施比阿拉斯加任何其他海象观赏点都要齐全。

让我对圆岛之行心生向往的原因之一，是它的行装清单：清单要求一顶能抵御每小时60英里大风的防风帐篷、一套帐杆修理工具，以及足够住上额外一周的食物。露营者必须为瞬息万变的天气做好准备。在这里，说了算的是大自然。

有些夜晚，风刮得如此猛烈，听上去仿佛帐篷就要从平台上被撕走。另一些夜晚，又静得让我在帐篷里就能听见海象的声音：呼吸、咆哮、呼哨，还有它们那空灵的鸣唱——那是空气在它们咽囊的一个个气室之间被推挤而发出的。多数清晨，当我走出帐篷，海风送来海雾，给草地镀上一层银光，愈发添了几分神话或梦境的意味。

海象是喜群居、爱扎堆、有“趋触性”的动物——也就是说，它们喜欢彼此紧挨在一起，连长牙也不例外。（人工圈养下失去父母的幼年海象，需要持续不断的身体接触才能存活。）它们成百上千地堆叠在圆岛的海滩上歇息，为争一个位置频繁地你推我搡，烦躁时便哼哼唧唧，用长牙互相戳来戳去。

在海上时，它们以蛤蜊之类的底栖无脊椎动物为食：平均每天要吃掉约一百磅，用被称为“触须”（vibrissae）的敏感胡须从海底把它们翻找出来，靠吸食吞下，只把贝壳留在原处。

每天有好几个钟头，我和哥哥在岛上众多的观景点之间流连，看着海象。当它们从海里游向沙滩时，姿态优雅，几近舞者；因长时间浸在冷水里而呈现苍白的粉色，看上去几乎是白的。随后它们笨重地爬上岸，浑身是脂肪和长牙，用前鳍拖着一身晃荡的赘肉，越过光滑的深色礁石。当它们加入岸上堆叠的数百头同类时，随着血液流向四肢以帮助散热，它们的皮肤会转为一种黯淡的玫瑰粉。

