“骨头”睁大了眼睛：“确实像。”她又端详了一遍屏幕上的影像。“我觉得你做的决定是对的，”她说。“我们该停下来。”

我当时并不知道，那个决定将会引出一项科学上的重大发现——以及我这一生中最惊恐、也最奇妙的一些时刻。

我们此前在迪纳莱迪的工作——2013年和2014年——成果惊人。在不到两个月的时间里，我的团队就从“启明星”洞内一处不足10平方英尺的地方，取出了1200多件化石，主要是骨骼和牙齿。

