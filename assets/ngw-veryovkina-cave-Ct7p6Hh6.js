洪峰——当突然聚积的水流从它能找到的任何开口猛冲而出——在洞穴里时常发生，起初我们并不担心。（后来我们才知道，地面上已经下了一个星期的雨。）我们那顶八人帐篷，扎在一道深渊半腰处的支洞里。我们以为自己不会挡在主水流的路上，便继续吃早饭。

那声音我永远忘不了，仿佛一列货运火车即将撞穿营地。它越来越响。所有人都张着嘴仰头望着，猜想黑暗中会冒出什么东西来。接着，一股巨大的急流从我们营地旁倾泻而过，直坠进更深的深渊。我们决定先等一等，看它会如何发展。有时候洪峰很快就过去了。

几个小时后，俄罗斯洞穴探险队员之一彼得·柳比莫夫，注意到营地边缘一个深洞里传来汩汩的水声——那是我们一直吐牙膏沫的地方。帕维尔和安德烈·舒瓦洛夫动身下去，查看洞系更深处的水位。

他们走后不久，彼得又去查看那个牙膏洞。当他转过身来，他惨白的脸说明了一切。洞里灌满了水。水位在上涨。我们必须快速行动。在营地里，我们只穿着贴身保暖层——毛绒绒的连体衣。我们手忙脚乱地在外面套上乳胶干式潜水服、Cordura防磨外套、安全带和攀爬装备。其他人对这套装备很熟练，动作很快。但在惊慌之中，我和杰夫得互相帮忙才能封好各自的干式潜水服。我的装备撒了一地。我从相机里抓出存储卡，装进一个密封袋，塞进胸前的口袋。其余的东西，我都丢下了。

营地周围的每一个洞，都在冒泡。

“我们现在就走，”我对杰夫说。

我们急忙横越一段之前绕开了一处50英尺落差的横切路段。那处落差如今成了一片湖，而我们离水面只有3英尺。我转身冲彼得喊道：“快点，彼得，我们必须撤离营地。”

他说他要等帕维尔和安德烈回来。我以为，我再也见不到他了。

我们用上升器，攀爬那些垂在竖井里、已经变成狂暴瀑布的绳索。我说不清自己是更害怕下方上涨的水，还是更害怕从头顶砸下来的激流。为了呼吸，我们低着头、收紧下巴，在头盔前沿下方留出一小块空气空间。每挪动一英寸都要拼尽全力，而我们还有将近600垂直英尺要爬。

我打头。如果我过不了某个障碍，后面所有人都会被困住、无路可逃，而水还在上涨。我慌了。我爬得太快，把杰夫甩得看不见了。我当真以为他和其他人都死了。这时，我听见身后传来一个非常愤怒的声音。杰夫正冲我大喊，叫我慢点。听到他的声音，我如释重负。终于，我们抵达了一条支洞里的临时露营点，可以安全地待在那里，避开积水和寒风。

其他洞穴探险队员中的第一个出现了。我们问他有没有见到别人。他说没有。我们心里认定其余的人都死了，虽然嘴上没说。我们继续前往下一个营地，等着。

接着，其他人陆续出现了。他们设法抓上了睡袋和一个炉子。所有人都活了下来，只是彼得的膝盖伤得很重。

我们没法再往上爬了，因为下一道瀑布通向一条狭窄的水平通道，那里此刻已被完全淹没。我们等了16个小时，被困在下方的洪水和上方无法通过的瀑布之间。

