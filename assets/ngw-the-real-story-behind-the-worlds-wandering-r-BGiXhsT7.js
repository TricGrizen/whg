漂砾小到巴掌大的卵石，大到房子那么大的巨岩，什么尺寸都有。它们静卧在田野里、山坡上，也沉在海底。但这些“流浪的石头”——就像图中这块，位于英格兰约克郡谷地、英格伯勒山附近——总是与周遭环境显得格格不入。

