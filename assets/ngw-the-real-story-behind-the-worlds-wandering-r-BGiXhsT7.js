它们的岩性，也就是物理成分，可能与它们最终落脚处的基岩截然不同，往往造就出令人惊奇的景象，因而成了游客慕名而来的看点，也成了当地传说的主角。

但这些石头本身会讲故事。对于懂得解读地貌的科学家来说，漂砾能透露出千百万年间地球那些冰封原野如何移动的线索。

“正是它们，让人们发现了范围广大得多的冰川作用——大陆冰川，也就是我们如今所说的冰河时代，”宾夕法尼亚州利哈伊大学的冰川学家爱德华·埃文森说。

随着如今的冰川消融、退缩，未来的漂砾很可能也会讲述人类造成的气候变化的故事。

