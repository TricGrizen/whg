塞缪尔·基斯佩自20世纪90年代以来，一直在乔克基劳当守卫、向导和修复工，他提出，这座建筑群是由昌卡人建造的——昌卡人在14、15世纪是邻近的阿普里马克地区印加人的对手。埃切瓦里亚不认同这一说法，他认为绝大多数建筑是在15世纪印加帝国扩张期间兴建的。

石羊驼的出土，把新的注意力引向了这片废墟，也催生了最新的一个传说——由于秘鲁政局的混乱，这个传说至今仍在不断发酵。

秘鲁前第一夫人埃利亚娜·卡普，在2002年为加快乔克基劳的修复争取资金援助上出力甚多。如今，她和她的丈夫、前总统亚历杭德罗·托莱多，因涉嫌腐败而受到调查。尽管这些指控没有一项与乔克基劳有关，但这场风波已使这对夫妇执政期间所做的一切都蒙上了疑云。种种传言四起，说卡普曾乘直升机进入乔克基劳，运走了一箱箱黄金文物。

卡普并没有运走黄金，但她的确力推过一条通往乔克基劳的缆车。理论上，这条索道对秘鲁旅游业和地方经济都可能有好处。但它至今仍在使各个社区、考古学家，以及那些必须批准其拨款的政客们意见分裂。

一个有力的、且早已围绕马丘比丘争论不休的论点，是游客对遗址的影响。2016年，联合国教科文组织曾因游客数量过多，威胁要把马丘比丘列入“濒危”名单。一套缆车系统不仅会给乔克基劳带来更多游客，还会带来那种可能损害这处脆弱遗址的基础设施。

一些人抱怨说，让大批人涌入这处圣地，可能会毁掉它那份偏远、未经发掘的魅力——也会毁掉如今把人们带到这里来的那些草根旅游生意。梅尔乔拉·普加在小径靠阿普里马克河一侧的奇基斯卡经营着食宿和一家餐馆，她担心缆车会迫使她和其他人放弃自己的生活方式。

“我们靠旅游为生。修缆车就好比砍断一棵树的根，却还指望这棵树能活。我们是活不下去的，”普加说。

如今已半退休、以赶骡为业的基斯佩说，缆车会断掉一大批服务提供者的生计——一趟快捷的缆车之旅，会把他们的营生统统绕过去。他有七个孩子，其中一个叫何塞·路易斯，是赶骡人，另一个则在政府修复乔克基劳更多梯田的项目里干活。这家人在小径沿途的科卡马萨纳经营着一家小店和一处露营地。

缆车之事至今悬而未决。但这并不困扰大多数铁杆徒步者。

“乔克基劳诱人的地方就在于它要花时间，所以你得下定决心才能去，”来自得克萨斯州休斯敦、26岁的麦迪逊·麦克唐纳说。她今年5月到访了乔克基劳。

