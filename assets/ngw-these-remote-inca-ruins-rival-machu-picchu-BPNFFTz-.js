马贝尔·科瓦鲁比亚斯一家在附近的马兰帕塔社区已住了一个多世纪，她说，直到20世纪80年代，她的祖辈还一直用这些梯田来种庄稼、放牲口。

对梯田的清理，把考古学家引向了那些石羊驼。它们白色的石质身躯与灰色的墙壁形成鲜明对比，透出层次和立体感。每天清晨，当第一缕阳光照到它们身上时，它们会把光反射出来。埃切瓦里亚说，这些羊驼梯田也许是作为献给太阳神的一种象征性供奉而建的——即便在没有真的活羊驼可供献祭的时候，也能顶上。

这一推测，只是关于这处遗址的一长串猜想之一。“围绕乔克基劳，存在着许多传说，”埃切瓦里亚说。的确，宾厄姆以及不少探险家和研究者，都曾就乔克基劳的起源、它与其他遗址的关系，以及它在印加帝国时期所扮演的角色，抛出过各种理论。

首先，有一个“立城传说”称，15世纪的印加抵抗领袖曼科·印加，在他对西班牙人发动的长达40年的游击战期间，曾有一段时间躲藏在这里的城堡中。

“这是个动听的故事，但和事实毫无关系，”埃切瓦里亚说。“我毫不怀疑曼科·印加到过乔克基劳，就像他也到过马丘比丘一样，但这座城并不是为抵抗运动而建的。”

