这只叫T12的老虎，一身深色的毛披在身上，像一件破旧的斗篷。星星点点的橙色从它身体各处透出来，在脸上和前腿上则显出较厚的斑块。老虎的黑色条纹这般诡异地变宽，是一种名为“伪黑化”的罕见基因突变，在游荡于西米利帕尔保护区的约30只老虎中，大约有一半都带有它。而它，也是一个保护成功故事正面临潜在灾难性并发症的信号。因为，尽管西米利帕尔的老虎数量比过去几十年都更为可观，这片保护区却在地理上与其他老虎种群相隔绝——可以说是一座“老虎孤岛”，其基因库正在危险地萎缩。

但就在拉古和我为寻找T12而在这一带四处搜索的那几周里，别处正在进行的，是为它物色一只合适配偶的工作。这是一项酝酿多年的定向繁育计划中的关键一步，是一项由保护工作者与一支开创性的分子生态学家和遗传学专家团队共同担负的使命，他们都在为把西米利帕尔的老虎从近亲繁殖以致灭绝的命运里拯救出来而努力。

在许多方面，印度的老虎面临着与世界各地大型猫科动物相同的挑战：在栖息地遭到无情破坏和割裂的同时，被猎取战利品的猎人捕杀到几近灭绝。20世纪70年代，人们对这一标志性物种数量下降的警觉，促成了一套国营保护区体系的建立。但这些保护区一直缺乏协调统一的监测和执法，直到2005年，印度成立了一个专门的中央机构——国家老虎保护局（NTCA），如今它在58个保护区内负责招募和培训护林员、管理科学监督并指导栖息地保护。

这套保护区体系背后的一个关键理念是：老虎通常能借助所谓的天然廊道——一片片相互连通的森林和其他猎物丰富的土地——在各保护区之间往来。这些廊道有诸多好处，但最重要的一条，是它们促进了相邻老虎种群之间的繁殖，从而提高遗传多样性。西米利帕尔面积略超一千平方英里，是印度最大的保护区之一，而离它最近的相邻保护区——西南面的萨特科西亚和东面的孙德尔本斯——都在一百多英里之外，这对一只老虎来说，倒也不算太远。

但萨特科西亚已经一只老虎都不剩了。而西米利帕尔与孙德尔本斯之间，也没有一条足够的廊道相连。两地之间的土地大多是城镇或农田——加尔各答及其近郊，还有一大片稻田——森林覆盖极少，而森林正是老虎喜欢藏身的地方。数十个城镇和村庄，也把西米利帕尔与它的两个相邻保护区隔开。对老虎而言，要进出西米利帕尔，实在没有一条好走的路。

NTCA在2006年对全印度的野生老虎做调查时，统计到大约1400只——比一个世纪前估计的4万只大为减少。在西米利帕尔，种群于2014年跌至谷底，只剩四只老虎，其中仅一只是雄性。但在2015年，也就是这只雄虎死前一年左右，它生下了T12——那只有着一身奇特、以黑色为主皮毛的老虎。此后，T12也生下了自己的雄性幼崽。

过去20年里，印度的老虎种群已开始回升，这在很大程度上要归功于NTCA和林业官员的保护工作。按2022年的估计，全国有3100多只老虎。而随着西米利帕尔的种群在过去十年里缓慢却稳定地攀升，老虎数量的增长起初似乎是这个全国性成功故事的一个缩影。然而没过多久，保护区的管理者们就开始注意到，越来越多的年轻老虎，长着与T12一样的深色皮毛。据护林员和遗传学家目前所知，这种突变是无害的，只不过是DNA一次随机、自然发生的小小反常所造成的外观上的怪异罢了。

但专家们说，它同时也是一个非常真切的问题的具体显现。如果这种突变能如此迅速地传遍西米利帕尔的整个种群——由于近亲繁殖泛滥，所有老虎的遗传构成都极为相似——那么更严重的异常同样可能如此传开。如今，印度一些最顶尖的老虎权威所面临的任务，已经从恢复老虎数量，转向趁还来得及打破这一近亲繁殖的循环。

