那是第50天傍晚将尽的时候，一转眼间，一个黑影从我们的皮卡车前猛地窜了出来。我狠踩刹车。在我们前方，一只硕大的老虎横占了整个路面，回瞪着拉古和我。从体型就看得出，这是一只上了年纪的雄虎——它身上正是我们一直在找的那身奇特而与众不同的皮毛。

“它是黑的，”拉古压着嗓子、语气笃定地说。他兴奋地指着，又重复了一遍。“它是黑的！”

