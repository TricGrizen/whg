与我一同寻访的搭档拉古·普尔蒂，是当地林业部门的一名工作人员，他从未亲眼见过T12。他的大多数同事，也只在为研究保护区各处动物活动而架设的相机陷阱所拍的影像里见过这只老虎。但真正亲眼看到西米利帕尔的老虎，能让林业官员查看那些相机也许捕捉不到的身体病症——也提醒着他们，自己在酷热中花去的无数巡逻时辰，是有一个活生生、会呼吸的目标的。若能有一次留下记录的T12目击，将格外珍贵，因为这只离群索居、10岁的老虎——西米利帕尔年纪最大的雄虎——当时正处在一项确保子孙后代存续的计划的核心。

