这片偏远之地每年吸引的游客不到一万人——只是美国其他国家公园的一个零头——它位于阿拉斯加内陆，完全处在北极圈以北。它超过840万英亩的辽阔，给无畏的探险者留足了纵情漫游的空间。

北极之门国家公园设立于1980年，是为了保护未经开发的土地——包括布鲁克斯山脉的一部分——以及原住民赖以为生的传统生计。它就连最坚韧的旅行者也要挑战一番。由于没有公路，多数游客从贝特尔斯、科尔德富特或阿纳克图沃克山口这几个聚落乘飞机进入，然后靠双脚、船只、滑雪板或狗拉雪橇继续前行。没有丰富荒野经验的人，应当考虑参加有向导带领的行程。

公园壮丽的景致庇护着种类繁多的动物。从五月到七月，永不落下的阳光意味着无穷无尽的观赏机会。

