凯利是州里的美洲狮生物学家，他朝东指向自己居住的那片铺展开来的住宅区。他说，就在四分之一英里外，一只美洲狮被相机拍到过；还有一只，穿过了我们身旁这条六车道的马路。

附近还住着另一只美洲狮，一头八岁的雌兽，编号FP224。她被汽车撞过两次，每次都断了一条腿。两次事故后，她都经兽医救治后放归。为了寻找她的踪迹，我们开车来到凯利家，房子旁有一小片森林，她最近就在那里筑巢，产下了至少三只幼崽。此时正值雨季，美洲狮的足迹通常会被雨水冲刷殆尽，但我们运气不错。

（杂交美洲狮正助这种珍稀猫科动物在佛罗里达重新壮大）

“她在这儿呢，”凯利指着松软沙地上一串硕大的爪印说，那爪印约莫有我拳头大小。我们循着爪印，穿过高大的松树和挂满附生植物的沙巴棕。查看凯利安在那里的一台由移动触发的红外相机，发现FP224就在两个晚上前、快到晚上9点时打这儿经过。

见到她的足迹令人激动——它提醒着人们，佛罗里达仍有荒野，仍有大型猫科动物，其中一些足够坚韧，能在不断扩张的郊区边缘悄无声息地存活。

大多数佛罗里达人一辈子也不会见到这些掠食者的任何踪迹——成年美洲狮视性别不同，体重在65到165磅之间，纵身一跃能超过10码。但美洲狮——切罗基人称之为“森林之王”——依赖着佛罗里达西南部和中部数百万英亩的沼泽、森林和田野，而其中许多正面临被开发的迫在眉睫的威胁。

佛罗里达美洲狮被归为山狮（即美洲狮，又称库加）的一个亚种，曾遍布美国东南部大部分地区。但它们遭到大肆猎杀，到20世纪70年代，就只在佛罗里达还能见到，数量已跌到不足30只，使它们极易陷入近亲繁殖的困境。它们曾一度濒临灭绝，凯利说。他身材瘦削，常穿着印有雇主——佛罗里达鱼类与野生动物保护委员会——徽标的衣服。

当年，科学家们谋划了一项前所未有的拯救方案：20世纪90年代中期，他们雇来得克萨斯人罗伊·麦克布赖德——大概算得上全世界最出色的山狮追踪者——在得州捕获八只美洲狮，全是雌性，再把它们放归南佛罗里达。其中五只成功繁殖，这次基因多样性的注入，扭转了美洲狮一路下滑的颓势。

种群缓慢恢复，如今大约有200只，大多分布在卡卢萨哈奇河以南一片辽阔而连绵的土地上——这条河自迈尔斯堡向东延伸。“这是美国历史上最富戏剧性的保护成功案例之一，”小卡尔顿·沃德说。他是一位环保人士兼摄影师，其工作得到美国国家地理学会的支持。

但形形色色的威胁给美洲狮的未来蒙上了阴影，其中车祸和同类间为争地盘的厮杀是两大头号死因。每年约有25只美洲狮死于车轮之下，这折射出在每天约有900人迁入佛罗里达之际，开发与修路正如何威胁着这一物种。

此外，一种病因不明、令其身体日渐衰弱的新型神经系统疾病，已侵袭了十几只美洲狮，让环保人士深感忧虑。

不过也有好消息：美洲狮正在收复一些昔日的领地。2016年，科学家自1973年以来第一次在卡卢萨哈奇河以北发现了一只雌兽。

“那是一个里程碑，”野生动物生物学家詹妮弗·科恩这样评价这次在巴布科克牧场保护区的目击。与雄兽不同，雌兽不会离母亲的活动范围太远，这是限制这一物种扩张的一大因素。

凯利估计，如今大约有二十几只美洲狮生活在卡卢萨哈奇河以北，其中包括几只雌兽。

沃德说，向北扩张，是美洲狮长久存续的必要条件，但这只有在“佛罗里达野生动物走廊”得到保护的前提下才可能实现——这条走廊由贯穿全州、公有与私有交错拼缀的土地组成。要做到这一点，就需要更多的保护资金，来帮助土地所有者（主要是牧场主）阻止开阔地变成住宅区、停车场和道路。

美洲狮向北复兴的核心地带，是一片被称为“大沼泽地源头”的地方，它属于为近一千万佛罗里达人供水的流域的一部分。环保人士说，发源于此的一部分水会流向南边的沼泽，因此投资保护这一区域，也会惠及大沼泽地。

佛罗里达许多美洲狮生活在公有土地上，包括大柏树国家保护区、佛罗里达美洲狮国家野生动物保护区、法卡哈奇滩州立保护公园和皮卡尤恩滩州立森林，这些地方合计约1420平方英里。

但州美洲狮生物学家戴夫·奥诺拉托说，这些以及南佛罗里达附近的保护区，无法供养规模大得多的美洲狮种群——它们是有领地意识的动物。他说，每只美洲狮或许需要多达200平方英里的领地，供其漫游和捕猎。与此同时，作为美洲狮主要食物来源之一的白尾鹿，其数量在大柏树的一些地区有所下降。这或许部分要归咎于入侵的缅甸蟒——它们会吞食鹿以及美洲狮的其他猎物。

当美洲狮向卡卢萨哈奇河以北挺进时，会遇到大片被大型牧场和农场占据的土地。道路从这许多地方中间穿过，区域内还星星点点散布着一些不大、却常在扩张的城镇。佛罗里达中南部较为知名的养牛场之一，是占地10500英亩的巴克岛牧场，由第六代佛罗里达人吉恩·洛利斯经营。

三月的一个清晨，日出之前，我骑着马和洛利斯一同出发。他戴着牛仔帽、蹬着靴子、穿着蓝色牛仔裤，同行的还有他的儿子洛朗和一群牛仔，一起去草原上把牛群赶拢——那片草原上，一丛丛菜棕和栎树像座座绿岛点缀其间。

和许多牧场一样，巴克岛牧场——归附近一处生态研究与教育机构阿奇博尔德生物站所有——为包括美洲狮在内的野生动物提供了至关重要的栖息地。

狗吠声中，牛仔们把牲口赶进围栏。我问身为佛罗里达养牛人协会会长的洛利斯，牧场主们怎么看待美洲狮。“我们对它们都挺有好感，”他说。“它们是这片天地的一部分。”

总的来说，牧场主和美洲狮面对着一个共同的敌人：开发，尤其是新建住宅。洛利斯说，每一个牧场主人都收到过开发商的报价，还说这件事对他而言切肤之痛——他年轻时干过活的、奥兰多附近的那些牧场，如今都成了住宅小区。

有些人把美洲狮的存在看作一件好事，因为它让某些地块被开发成郊区变得更难。

“《濒危物种法》上，你看得到写着‘牛仔’或‘牧场主’吗？没有，但我们沾了美洲狮所受保护的光，”住在西边、阿卡迪亚附近的牧场主埃尔顿·兰福德说。

但有些牧场主更为提防，尤其是在美洲狮更多的南部，塞米诺尔族牛倌亚历克斯·约翰斯说。他家自16世纪祖先从西班牙人那儿偷牛以来，就一直经营牧场。

在这一带，美洲狮偶尔会吃掉小牛。一项在某牧场进行的研究发现，美洲狮咬死的小牛不到1%；另一项研究则发现，这些掠食者咬死了约5%。

大柏树国家保护区的美洲狮生物学家黛博拉·扬森说，有时候，郊狼、熊、甚至美洲鹫咬死的牲口，也被算到了美洲狮头上。她自20世纪80年代初起就一直和这些猫科动物打交道。

牛倌约翰斯说，小牛的损失会激起怨气，甚至招来报复。他还说，更糟的是，联邦政府那套为牧场主因美洲狮而损失牲畜提供补偿的项目本身就有缺陷——他形容那套流程文书繁琐、耗时费力，而且补偿常常批不下来。

美国鱼类与野生动物管理局的美洲狮协调员戴维·欣德尔也认为这套补偿项目需要改进，他把双方视为盟友。“要拯救美洲狮，我们就得拯救牧场主，”他说。

欣德尔补充说，美洲狮的倡导者们需要想办法，让这些动物出现在土地上这件事对地主有利可图——在卡卢萨哈奇河以北，土地大多是私有的。做到这一点的一个途径，是鼓励公共和私人资金投入“保护地役权”：买断土地的开发权，同时允许所有者继续耕作和放牧。

环保人士说，对美洲狮更为迫近的一大危险，是一项名为M-CORES的大型收费公路网提案。其中一段将从奥兰多附近到那不勒斯，划出一条140英里长的通道。

这段公路遭到环保人士和许多牧场主的坚决反对，它将横切野生动物走廊的一些地段，以及佛罗里达西南内陆最后一些尚未开发的地方。

除此之外，科学家还发现了一种名为“猫白质脊髓病”的神经系统疾病，佛罗里达的美洲狮和短尾猫都会患上。染病的动物常会踉跄或行走困难，严重时可导致瘫痪、饥饿乃至死亡。

据州生物学家称，截至2020年12月，据信已有26只短尾猫和18只美洲狮患上此病。扬森说，仅在大柏树，就很可能已有三只美洲狮因此死亡。这种病症的成因尚不清楚，但推测包括接触有毒化学物质，或感染病毒之类的病原体。

扬森说，大多数患病的动物是在毗邻已开发土地的地带发现的；而在大柏树这样更为原始的地区也出现病例，令人担忧。

由于美洲狮面临的种种威胁，扬森认为“佛罗里达美洲狮的前途大有疑问”——这也正是为什么扩大它们栖息地如此关键的一大原因。

凯利则更为乐观。他说，只要有足够的绿地和野生动物走廊得到保护，美洲狮在几十年内就有可能一路抵达佛罗里达北部，甚至扩散到其他拥有丰富美洲狮栖息地的州，包括佐治亚。2008年，一只出生在大沼泽地附近的雄性美洲狮，一路到达佐治亚西部——大约在佛罗里达州界以北一百英里处——后来被一名猎鹿人开枪打死。

与此同时，凯利和同事们在卡卢萨哈奇河以北的各处安放了大约一百台相机，以进一步了解美洲狮如何移动、往哪里去。最近，一只雌兽在费什伊廷溪一带缓步走过，巴布科克也有另一只，两只都被拍到与雄兽同框。凯利说，这值得庆贺，因为成对的美洲狮往往会产下幼崽。

秋日的一个下午，我和凯利一起，穿过泥泞、茂密的灌木丛，走到巴布科克牧场保护区一条小溪的岸边。他指着一丛缠结的沙巴棕，回忆起一个月前在这里见到过一只美洲狮。

“我们就那样对望了大约20分钟，”凯利说。他很快就凭那只美洲狮娇小的体型、以及她正发出的哀号——一种发情的迹象——认出这是一只雌兽。

那一刻意义非凡——这是自1973年山狮追踪者罗伊·麦克布赖德在费什伊廷溪发现一只年老雌兽以来，第一次有人在卡卢萨哈奇河以北亲眼确认见到美洲狮。

这只新发现的雌兽，是“我的白鲸”，凯利说。

随后，我们乘着一辆沼泽车穿过这片地区，向北行进20英里，越过被水淹没的田野，在矮棕榈丛和柏树林岛之间蜿蜒穿行。这里野生动物众多：运气好的日子，你能看到熊、水獭、短吻鳄，还有凤头卡拉鹰、燕尾鸢等鸟类，它们和美洲狮一样，都依赖着同一片荒野。

凯利停下来查看一台不久前才安好、拴在一棵栎树上的相机。他翻看着照片，在一众“常客”——郊狼、野猪、浣熊、鹿——之间，夹着一张美洲狮的照片，是几周前经过时拍下的。

而且不是随便哪只美洲狮：这是一只身形颀长、生物学家从未见过的雌兽，正沿着那道把小溪与相邻牧场隔开的围栏北侧大步走着——也许，是走向北方的新生活。

本刊撰稿人道格拉斯·梅因曾在2019年3月号上撰文介绍角蝉。小卡尔顿·沃德为唤起人们对佛罗里达州野生动物走廊的关注，已徒步走遍全州2000多英里。

