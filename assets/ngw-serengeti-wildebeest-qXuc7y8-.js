那条线出现在地平线上时，像是浅绿色被面上的一根灰线；可当飞机靠近，它化作了一支由几百头动物组成的纵队，蜿蜒着穿过平原。“角马，”查理压过引擎的轰鸣喊道，“是一小群。”我们正在坦桑尼亚恩戈罗恩戈罗火山口的北面。当时是三月，我们知道角马很快就会朝西北移动，穿过塞伦盖蒂国家公园，进入肯尼亚。

它们就在那儿，排成一列笔直的、首尾相衔的队伍。我能看清它们弯曲的犄角，还有那一颗颗长长的脑袋，随着它们在晨光里跋涉而一上一下地点动。几头幼崽紧紧贴着母亲的胁腹。

几千年来，角马群沿着一条顺时针的环线，在大塞伦盖蒂生态系统里迁徙——每头动物要迂回走上约1750英里，相当于从缅因州波特兰到佛罗里达州基韦斯特的距离——追着雨水，啃食青草，肥沃土地，成为捕食者的口粮。此刻，踏着祖先亘古不变的路径，这支队伍正朝西北走去。

可是等等，它们并没有朝西北走。

“它们为什么往南走？”我朝查理喊。

“鬼才知道，”他答道，“它们在找草。这儿没多少吃的。”

我来坦桑尼亚，是为了看角马大迁徙，并与查理·汉密尔顿·詹姆斯会合——他已经花了两年记录它们的长途跋涉。我们从阿鲁沙起飞，乞力马扎罗山在地平线上隐隐耸立。大地在脚下铺展成一片葱郁的绿色海洋，是咖啡种植园与茂密林丛拼缀成的织锦；可当我们飞过火山口，地形便让位给辽阔的平原——它由远古的熔岩流铸成，上面覆着邻近火山喷来的一层层肥沃火山灰。（火山喷发如何滋养世界）

就在一个月前，我们下方这片土地还是一层营养丰富的青草地毯，但雨季已经结束；如今放眼几乎每一个方向，地面都显得干枯，只剩一丝丝草影。这支角马纵队，像是一个迷路的、流浪的部落，暴露在旷野之中，成了狮群或一家鬣狗轻而易举的猎物。

这时我注意到一头角马迈出了队列。它四下张望，朝相反方向走去，仿佛已经断定这群同伴走错了方向，决意独自另辟出路。对一头离群的动物来说，这几乎等于必死。整群角马没理会这个叛逆者，继续缓步前行。我心想，那头角马，死定了。

想想前方那一道道障碍，你会得出“这群里许多都难逃一死”的结论，而且没错。它们将任凭反复无常的天气摆布，不断修正路线，长途跋涉去寻找新鲜的草场。它们将被捕食者没完没了地劫掠。近些年，它们还得对付人为的阻碍——为保护庄稼和牛群而竖起的围栏——以及来自日渐壮大的绵羊群、山羊群的竞争。

但也许最令它们生畏的考验，是一道古老的难关：马拉河。要抵达肯尼亚马赛马拉国家保护区里最好的草场，它们必须渡过这条河；返回坦桑尼亚时，还得再渡一次。查理在塞伦盖蒂拍摄影像和照片已有二十多年，见过几十次渡河，眼看着成千上万头角马懵懵懂懂地跟着彼此走向死亡。“去年我就在这儿目睹了，几百具尸体堆在河岸上、漂在河里，”他对我说，“简直是场血腥的噩梦。”

